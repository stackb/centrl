// NewLanguage is called by Gazelle to install this language extension in a
// binary.
func NewLanguage() language.Language {
	return newBcrExtension()
}

// newBcrExtension create a new Gazelle extension implementation.
func newBcrExtension() *bcrExtension {
	return &bcrExtension{}
}

// bcrExtension implements language.Language.
type bcrExtension struct {
	name string
}

// Name returns the name of the language. This should be a prefix of the kinds
// of rules generated by the language, e.g., "go" for the Go extension since it
// generates "go_library" rules.
func (pl *bcrExtension) Name() string {
	return "bcr"
}

