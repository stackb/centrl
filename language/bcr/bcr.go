// Imports returns a list of ImportSpecs that can be used to import the rule r.
// This is used to populate RuleIndex.
//
// If nil is returned, the rule will not be indexed. If any non-nil slice is
// returned, including an empty slice, the rule will be indexed.
func (pl *bcrExtension) Imports(c *config.Config, r *rule.Rule, f *rule.File) []resolve.ImportSpec {
	// Only handle module_version rules
	if r.Kind() != "module_version" {
		return nil
	}

	// Get the module name and version to construct the import spec
	moduleName := r.AttrString("module_name")
	version := r.AttrString("version")

	if moduleName == "" || version == "" {
		return nil
	}

	// Construct and return the import spec: "module_name@version"
	importSpec := resolve.ImportSpec{
		Lang: "bcr",
		Imp:  fmt.Sprintf("%s@%s", moduleName, version),
	}

	return []resolve.ImportSpec{importSpec}
}

// Embeds returns a list of labels of rules that the given rule embeds. If a
// rule is embedded by another importable rule of the same language, only the
// embedding rule will be indexed. The embedding rule will inherit the imports
// of the embedded rule. Since SkyLark doesn't support embedding this should
// always return nil.
func (*bcrExtension) Embeds(r *rule.Rule, from label.Label) []label.Label {
	return nil
}

// Resolve translates imported libraries for a given rule into Bazel
// dependencies. Information about imported libraries is returned for each rule
// generated by language.GenerateRules in language.GenerateResult.Imports.
// Resolve generates a "deps" attribute (or the appropriate language-specific
// equivalent) for each import according to language-specific rules and
// heuristics.
func (pl *bcrExtension) Resolve(
	c *config.Config,
	ix *resolve.RuleIndex,
	rc *repo.RemoteCache,
	r *rule.Rule,
	importsRaw any,
	from label.Label,
) {
	// Only handle module_dependency rules
	if r.Kind() != "module_dependency" {
		return
	}

	// Get the dependency name and version to construct the import spec
	depName := r.AttrString("dep_name")
	version := r.AttrString("version")

	if depName == "" || version == "" {
		log.Printf("module_dependency %s missing dep_name or version", r.Name())
		return
	}

	// Construct the import spec: "module_name@version"
	importSpec := resolve.ImportSpec{
		Lang: "bcr",
		Imp:  fmt.Sprintf("%s@%s", depName, version),
	}

	// Find the module_version rule that provides this import
	results := ix.FindRulesByImport(importSpec, "bcr")

	if len(results) == 0 {
		log.Printf("No module_version found for %s@%s", depName, version)
		return
	}

	// Use the first result (should only be one)
	result := results[0]

	// Set the module attr to point to the found module_version rule
	r.SetAttr("module", result.Label.String())
}

// GenerateRules extracts build metadata from source files in a directory.
// GenerateRules is called in each directory where an update is requested in
// depth-first post-order.
//
// args contains the arguments for GenerateRules. This is passed as a struct to
// avoid breaking implementations in the future when new fields are added.
//
// A GenerateResult struct is returned. Optional fields may be added to this
// type in the future.
//
// Any non-fatal errors this function encounters should be logged using
// log.Print.
func (pl *bcrExtension) GenerateRules(args language.GenerateArgs) language.GenerateResult {
	log.Println("visiting:", args.Rel, args.RegularFiles)

	var rules []*rule.Rule

	for _, name := range args.RegularFiles {
		if name == "metadata.json" {
			filename := filepath.Join(args.Config.WorkDir, args.Rel, name)
			md, err := readMetadataJson(filename)
			if err != nil {
				log.Panicln(err)
			}
			// Generate maintainer rules
			maintainerRules := makeModuleMaintainerRules(md.Maintainers)
			// Add maintainer rules to the list
			rules = append(rules, maintainerRules...)
			// Add metadata rule with references to maintainers
			rules = append(rules, makeModuleMetadataRule(md, maintainerRules))
		}
		if name == "MODULE.bazel" && strings.Contains(args.Rel, "modules/") && !strings.Contains(args.Rel, "overlay/") {
			filename := filepath.Join(args.Config.WorkDir, args.Rel, name)
			module, err := readModuleBazelFile(filename)
			if err != nil {
				log.Panicln(err)
			}
			// Try to read source.json if it exists
			var sourceRule *rule.Rule
			sourceFilename := filepath.Join(args.Config.WorkDir, args.Rel, "source.json")
			source, err := readSourceJson(sourceFilename)
			if err != nil {
				// source.json is optional, just log if missing
				log.Printf("No source.json found for %s: %v", args.Rel, err)
			} else {
				module.Source = source
				// Generate source rule
				sourceRule = makeModuleSourceRule(source)
				rules = append(rules, sourceRule)
			}
			// Try to read attestations.json if it exists
			var attestationsRule *rule.Rule
			attestationsFilename := filepath.Join(args.Config.WorkDir, args.Rel, "attestations.json")
			attestations, err := readAttestationsJson(attestationsFilename)
			if err != nil {
				// attestations.json is optional, just log if missing
				log.Printf("No attestations.json found for %s: %v", args.Rel, err)
			} else {
				module.Attestations = attestations
				// Generate attestations rule
				attestationsRule = makeModuleAttestationsRule(attestations)
				rules = append(rules, attestationsRule)
			}
			// Extract version from path (e.g., modules/foo/1.2.3 -> 1.2.3)
			version := filepath.Base(args.Rel)
			// Generate dependency rules
			depRules := makeModuleDependencyRules(module.Deps)
			// Add dependency rules to the list
			rules = append(rules, depRules...)
			// Add module version rule with references to dependencies, source, and attestations
			rules = append(rules, makeModuleVersionRule(module, version, depRules, sourceRule, attestationsRule))
		}
	}

	imports := make([]interface{}, len(rules))
	for i, r := range rules {
		imports[i] = r.PrivateAttr(config.GazelleImportsKey)
	}

	return language.GenerateResult{
		Gen:     rules,
		Imports: imports,
	}
}
