// Imports returns a list of ImportSpecs that can be used to import the rule r.
// This is used to populate RuleIndex.
//
// If nil is returned, the rule will not be indexed. If any non-nil slice is
// returned, including an empty slice, the rule will be indexed.
func (ext *bcrExtension) Imports(c *config.Config, r *rule.Rule, f *rule.File) []resolve.ImportSpec {
	switch r.Kind() {
	case "module_version":
		return moduleVersionImports(r)
	case "module_metadata":
		return moduleMetadataImports(r)
	case "git_override":
		return gitOverrideImports(r)
	case "archive_override":
		return archiveOverrideImports(r)
	case "single_version_override":
		return singleVersionOverrideImports(r)
	case "local_path_override":
		return localPathOverrideImports(r)
	case "repository_metadata":
		return repositoryMetadataImports(r)
	}
	return nil
}

