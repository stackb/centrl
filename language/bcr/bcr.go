func (*bcrExtension) RegisterFlags(fs *flag.FlagSet, cmd string, c *config.Config) {
}

func (*bcrExtension) CheckFlags(fs *flag.FlagSet, c *config.Config) error {
	return nil
}

func (*bcrExtension) KnownDirectives() []string {
	return nil
}

// Configure implements config.Configurer
func (ext *bcrExtension) Configure(c *config.Config, rel string, f *rule.File) {
}

// Kinds returns a map of maps rule names (kinds) and information on how to
// match and merge attributes that may be found in rules of those kinds. All
// kinds of rules generated for this language may be found here.
func (*bcrExtension) Kinds() map[string]rule.KindInfo {
	kinds := map[string]rule.KindInfo{}
	maps.Copy(kinds, moduleMetadataKinds())
	maps.Copy(kinds, moduleMaintainerKinds())
	maps.Copy(kinds, moduleVersionKinds())
	maps.Copy(kinds, moduleDependencyKinds())
	maps.Copy(kinds, moduleSourceKinds())
	maps.Copy(kinds, moduleAttestationsKinds())
	maps.Copy(kinds, modulePresubmitKinds())
	maps.Copy(kinds, moduleDependencyCycleKinds())
	maps.Copy(kinds, moduleRegistryKinds())
	maps.Copy(kinds, gitOverrideKinds())
	maps.Copy(kinds, archiveOverrideKinds())
	maps.Copy(kinds, singleVersionOverrideKinds())
	maps.Copy(kinds, localPathOverrideKinds())
	return kinds
}

